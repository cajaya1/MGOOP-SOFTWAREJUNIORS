    private void BtnEditDressActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditDressActionPerformed
        DressController dressController = new DressController();
        FormData formData = new FormData();
        
        formData.setName(TxtDressName.getText());
        formData.setPrice(TxtDressPrice.getText());
        formData.setQuantity(TxtDressQuantity.getText());
        formData.setSize(TxtDressSize.getText());
        formData.setActualDate(TxtRegisteredDate.getText());
        
        InventoryController inventoryController = new InventoryController();
        String dressData = dressController.convertFormToDress(formData);
        inventoryController.editDressOfDb(selectedDressId, dressData);
        
        PnlInventory pnlInventory = new PnlInventory();
        pnlInventory.setVisible(true);
        pnlInventory.setLocationRelativeTo(this);
        this.dispose();
    }//GEN-LAST:event_BtnEditDressActionPerformed

